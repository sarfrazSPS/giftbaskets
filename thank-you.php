    // Function to populate the thank-you page with dynamic data
    function populateThankYouPage(data) {
        document.getElementById('transactionId').innerText = data.transaction_id;
        document.getElementById('value').innerText = data.value.toFixed(2);
        document.getElementById('currency').innerText = data.currency;

        // Populate purchased items
        const itemsList = document.getElementById('items');
        itemsList.innerHTML = ''; // Clear any existing content
        data.items.forEach(item => {
            let listItem = document.createElement('li');
            listItem.innerText = `${item.name} - Quantity: ${item.quantity}, Price: $${item.price}`;
            itemsList.appendChild(listItem);
        });

        document.getElementById('shipping').innerText = data.shipping.toFixed(2);
        document.getElementById('coupon').innerText = data.coupon;
        document.getElementById('userEmail').innerText = data.user_email;
        document.getElementById('userPhone').innerText = data.user_phone;
        document.getElementById('userName').innerText = data.user_name;
    }

    // Call the function on page load
    document.addEventListener('DOMContentLoaded', () => {
        populateThankYouPage(transactionData);
